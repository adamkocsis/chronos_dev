			"lithdescript",     
			"lithology1",
			"lithification1",
			"minor_lithology1", 
			"lithology2",
			"lithification2",
			"minor_lithology2", 
			"environment",
			"primary_reference",
			"created",          
			"modified",
			"time_bins",
			"time_contain",     
			"time_major",
			"time_buffer",
			"time_overlap")

		return(pbdb[,cols])
	}, 
	ns="chronosphere")

