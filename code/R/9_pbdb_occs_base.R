			"record_type",
			"reid_no",          
			"flags",
			"collection_no",
			"identified_name",  
			"identified_rank",
			"identified_no",
			"difference",       
			"accepted_name",
			"accepted_rank",
			"accepted_no",      
			"early_interval",
			"late_interval",
			"max_ma",           
			"min_ma",
			"reference_no",
			"phylum",           
			"phylum_no",
			"class",
			"class_no",         
			"order",
			"order_no",
			"family",           
			"family_no",
			"genus",
			"genus_no",         
			"subgenus_no",
			"collection_name",
			"collection_subset",
			"collection_aka",
			"lng",
			"lat",              
			"cc",
			"state",
			"county",           
			"latlng_basis",
			"latlng_precision",
			"geogscale",        
			"geogcomments",
			"paleomodel",
			"paleolng",         
			"paleolat",
			"geoplate",
			"formation",        
			"stratgroup",
			"member",
			"stratscale",       
